    private void btnThemVoPhieuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemVoPhieuActionPerformed
        // TODO add your handling code here:
         // L·∫•y d·ªØ li·ªáu t·ª´ c√°c √¥ nh·∫≠p
        String maSP = txtMaSP.getText().trim();
        String tenSP = txtTenSP.getText().trim();
        String soLuongStr = txtSoLuong.getText().trim();
        String donGiaStr = txtDonGia.getText().trim();

        if (maSP.isEmpty() || tenSP.isEmpty() || soLuongStr.isEmpty() || donGiaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui l√≤ng nh·∫≠p ƒë·∫ßy ƒë·ªß th√¥ng tin!");
            return;
        }

        try {
            int soLuong = Integer.parseInt(soLuongStr);
            double donGia = Double.parseDouble(donGiaStr);

            DefaultTableModel model = (DefaultTableModel) tblKetQua.getModel();

            // X√≥a c√°c d√≤ng r·ªóng ch·ªâ khi ch∆∞a c√≥ d·ªØ li·ªáu h·ª£p l·ªá
            if (model.getRowCount() > 0) {
                boolean coHangRong = true;
                for (int i = 0; i < model.getRowCount(); i++) {
                    boolean rong = true;
                    for (int j = 0; j < model.getColumnCount(); j++) {
                        if (model.getValueAt(i, j) != null) {
                            rong = false;
                            break;
                        }
                    }
                    if (!rong) {
                        coHangRong = false;
                        break;
                    }
                }
                if (coHangRong) {
                    model.setRowCount(0); // X√≥a n·∫øu t·∫•t c·∫£ ƒë·ªÅu r·ªóng
                }
            }

            boolean isProductExist = false;
            for (int i = 0; i < model.getRowCount(); i++) {
                // Ki·ªÉm tra m√£ s·∫£n ph·∫©m ƒë√£ t·ªìn t·∫°i trong b·∫£ng ch∆∞a
                Object existingMaSPObj = model.getValueAt(i, 1);  // C·ªôt m√£ s·∫£n ph·∫©m ·ªü v·ªã tr√≠ 1
                if (existingMaSPObj != null) {
                    String existingMaSP = existingMaSPObj.toString();
                    if (maSP.equals(existingMaSP)) {
                        // N·∫øu ƒë√£ t·ªìn t·∫°i, ch·ªâ c·∫ßn tƒÉng s·ªë l∆∞·ª£ng
                        int existingSoLuong = Integer.parseInt(model.getValueAt(i, 3).toString());
                        model.setValueAt(existingSoLuong + soLuong, i, 3);  // C·∫≠p nh·∫≠t s·ªë l∆∞·ª£ng
                        isProductExist = true;
                        break;  // N·∫øu t√¨m th·∫•y, kh√¥ng c·∫ßn ki·ªÉm tra n·ªØa
                    }
                }
            }

            if (!isProductExist) {
                // N·∫øu ch∆∞a c√≥ s·∫£n ph·∫©m trong b·∫£ng, th√™m m·ªõi
                int stt = model.getRowCount() + 1;
                Vector row = new Vector();
                row.add(stt);
                row.add(maSP);
                row.add(tenSP);
                row.add(soLuong);
                row.add(donGia);
                model.addRow(row);
            }

            // C·∫≠p nh·∫≠t l·∫°i s·ªë th·ª© t·ª± (STT) c·ªßa c√°c d√≤ng trong b·∫£ng
            for (int i = 0; i < model.getRowCount(); i++) {
                model.setValueAt(i + 1, i, 0);  // C·∫≠p nh·∫≠t s·ªë th·ª© t·ª± (c·ªôt 0)
            }

            // T√≠nh t·ªïng ti·ªÅn
            double tongTien = 0;
            for (int i = 0; i < model.getRowCount(); i++) {
                Object slObj = model.getValueAt(i, 3);
                Object dgObj = model.getValueAt(i, 4);
                if (slObj != null && dgObj != null) {
                    try {
                        int sl = Integer.parseInt(slObj.toString());
                        double dg = Double.parseDouble(dgObj.toString());
                        tongTien += sl * dg;
                    } catch (NumberFormatException ex) {}
                }
            }
            txtTongTien.setText(String.format("%.0f", tongTien));

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "S·ªë l∆∞·ª£ng v√† ƒë∆°n gi√° ph·∫£i l√† s·ªë h·ª£p l·ªá!");
        }
    }//GEN-LAST:event_btnThemVoPhieuActionPerformed

