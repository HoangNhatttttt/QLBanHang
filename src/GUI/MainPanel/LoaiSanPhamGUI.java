    private void loadDataToTable() {
        String[] header = {"Mã Loại", "Tên Loại", "Mô Tả"};
        DefaultTableModel model = new DefaultTableModel(header, 0);

        LoaiSanPhamBUS bus = new LoaiSanPhamBUS();
        List<LoaiSanPhamDTO> dsLSP = bus.getDsLSP();
    
        if (dsLSP != null) {
            for (LoaiSanPhamDTO lsp : dsLSP) {
                Object[] row = {
                    lsp.getMaLoai(),
                    lsp.getTenLoai(),
                    lsp.getMoTa()
                };
                model.addRow(row);
            }
        }
        tblDS_LSP.setModel(model);
    }
