    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {
        // Lấy dữ liệu từ các trường text
        String maSP = txtMaSanPham.getText().trim();
        String tenSP = txtTenSP.getText().trim();
        String donGiaStr = txtDonGia.getText().trim().replace(",", ""); // Loại bỏ dấu phẩy
        String soLuongStr = txtSoLuong.getText().trim();
        
        // Kiểm tra dữ liệu đầu vào
        if (maSP.isEmpty() || tenSP.isEmpty() || donGiaStr.isEmpty() || soLuongStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm và nhập đủ thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            // Chuyển đổi số lượng và đơn giá
            int soLuong = Integer.parseInt(soLuongStr);
            double donGia = Double.parseDouble(donGiaStr);
            
            // Kiểm tra số lượng
            if (soLuong <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Lấy số lượng tồn kho từ bảng sản phẩm
            int selectedRow = tblSanpham.getSelectedRow();
            if (selectedRow < 0) {
                JOptionPane.showMessageDialog(this, "Vui lòng chọn sản phẩm!", "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            int tonKho = 0;
            try {
                String tonKhoStr = tblSanpham.getValueAt(selectedRow, 4).toString();
                tonKho = Integer.parseInt(tonKhoStr);
            } catch (Exception e) {
                System.out.println("Lỗi khi lấy số lượng tồn kho: " + e.getMessage());
                tonKho = 0;
            }
            
            // Kiểm tra số lượng đã có trong giỏ hàng
            DefaultTableModel gioHangModel = (DefaultTableModel) tblGioHang.getModel();
            int soLuongTrongGio = 0;
            
            for (int i = 0; i < gioHangModel.getRowCount(); i++) {
                Object maSPObj = gioHangModel.getValueAt(i, 1);
                if (maSPObj != null && maSP.equals(maSPObj.toString())) {
                    Object soLuongObj = gioHangModel.getValueAt(i, 3);
                    if (soLuongObj != null) {
                        soLuongTrongGio = Integer.parseInt(soLuongObj.toString());
                    }
                    break;
                }
            }
            
            // Kiểm tra nếu tổng số lượng vượt quá tồn kho
            if (soLuong + soLuongTrongGio > tonKho) {
                JOptionPane.showMessageDialog(this, 
                    "Số lượng vượt quá tồn kho!\nTồn kho: " + tonKho + 
                    "\nĐã chọn: " + soLuongTrongGio +
                    "\nCòn lại: " + (tonKho - soLuongTrongGio), 
                    "Lỗi", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Kiểm tra sản phẩm đã có trong giỏ hàng chưa
            DefaultTableModel model = (DefaultTableModel) tblGioHang.getModel();
            boolean found = false;
            int rowIndex = -1;
            
            for (int i = 0; i < model.getRowCount(); i++) {
                Object valueObj = model.getValueAt(i, 1);
                if (valueObj != null && maSP.equals(valueObj.toString())) {
                    found = true;
                    rowIndex = i;
                    break;
                }
            }
            
            if (found) {
                // Nếu sản phẩm đã có trong giỏ hàng, cập nhật số lượng
                Object soLuongObj = model.getValueAt(rowIndex, 3);
                int soLuongCu = soLuongObj != null ? Integer.parseInt(soLuongObj.toString()) : 0;
                int soLuongMoi = soLuongCu + soLuong;
                double thanhTien = soLuongMoi * donGia;
                
                model.setValueAt(soLuongMoi, rowIndex, 3);
                model.setValueAt(String.format("%,.0f", thanhTien), rowIndex, 5);
            } else {
                // Nếu là sản phẩm mới, thêm dòng mới
                double thanhTien = soLuong * donGia;
                
                // Tìm STT tiếp theo
                int stt = model.getRowCount() + 1;
                
                // Thêm dòng mới vào bảng
                model.addRow(new Object[]{
                    stt,
                    maSP,
                    tenSP,
                    soLuong,
                    String.format("%,.0f", donGia),
                    String.format("%,.0f", thanhTien)
                });
            }
            
            // Cập nhật tổng tiền
            applyKhuyenMai();
            
            // Reset lại số lượng
            txtSoLuong.setText("1");
            
            // Đảm bảo dữ liệu được hiển thị
            model.fireTableDataChanged();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng và đơn giá phải là số!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            System.out.println("Lỗi: " + e.getMessage());
        }
    }

    private void capNhatTongTien() {
        DefaultTableModel model = (DefaultTableModel) tblGioHang.getModel();
        double tongTien = 0;
        
        for (int i = 0; i < model.getRowCount(); i++) {
            Object thanhTienObj = model.getValueAt(i, 5);
            if (thanhTienObj != null) {
                String thanhTienStr = thanhTienObj.toString().replace(",", "");
                tongTien += Double.parseDouble(thanhTienStr);
            }
        }
        
        // Hiển thị tổng tiền
        txtTongTien.setText(String.format("%,.0f", tongTien));
        
        // Cập nhật thành tiền (tổng tiền sau khi trừ giảm giá)
        double giamGia = 0;
        try {
            String giamGiaStr = txtGiamGia.getText().trim().replace(",", "");
            if (!giamGiaStr.isEmpty()) {
                giamGia = Double.parseDouble(giamGiaStr);
            }
        } catch (NumberFormatException e) {
            giamGia = 0;
        }
        
        double thanhTien = tongTien - giamGia;
        if (thanhTien < 0) thanhTien = 0;
        txtThanhTien.setText(String.format("%,.0f", thanhTien));
        
        //System.out.println(tongTien + " - " + giamGia + " = " + thanhTien);
    }


