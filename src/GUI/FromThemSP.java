    private void btnthemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnthemActionPerformed
        String maSP = txtmasp.getText().trim();
        String tenSP = txttensp.getText().trim();
        String donVi = txtdonvi.getText().trim();
        float donGia = Float.parseFloat(txtdongia.getText());
        
        String maLoai = txtmaloai.getText().trim();
        String hinhAnh = txthinhanh.getText().trim();
        
        
        if (maSP.isEmpty() || tenSP.isEmpty() || donVi.isEmpty() || maLoai.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin.");
            return;
        }
        String tenLoai = LoaiSanPhamBUS.getTenLoaiByMa(maLoai);
        SanPhammDTO sp = new SanPhammDTO(maSP, tenSP, 0, donGia, donVi, maLoai, hinhAnh, tenLoai);
        SanPhammBUS bus = new SanPhammBUS(DBConnect.Connector.getConnection());
        try {
            if (bus.insertSanPham(sp)) {
                JOptionPane.showMessageDialog(null, "Cập nhật thành công!");
                dispose(); // đóng form
            } else {
                JOptionPane.showMessageDialog(null, "Cập nhật thất bại!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(FromSuaSP.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnthemActionPerformed

    private void btntruycapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntruycapActionPerformed
        // TODO add your handling code here:
        javax.swing.JFileChooser fileChooser = new javax.swing.JFileChooser();
        fileChooser.setDialogTitle("Chọn ảnh sản phẩm");
        fileChooser.setFileSelectionMode(javax.swing.JFileChooser.FILES_ONLY);

        // Lọc chỉ file ảnh (jpg, png, jpeg)
        javax.swing.filechooser.FileNameExtensionFilter filter = new javax.swing.filechooser.FileNameExtensionFilter(
            "Hình ảnh", "jpg", "jpeg", "png"
        );
        fileChooser.setFileFilter(filter);

        int result = fileChooser.showOpenDialog(this);
        if (result == javax.swing.JFileChooser.APPROVE_OPTION) {
            java.io.File selectedFile = fileChooser.getSelectedFile();
            txthinhanh.setText(selectedFile.getAbsolutePath());           
        }
    }//GEN-LAST:event_btntruycapActionPerformed

