    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblDS_LSP.getSelectedRow();

        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một dòng để xóa!");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc muốn xóa loại sản phẩm này?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        String maLoai = tblDS_LSP.getValueAt(selectedRow, 0).toString();

        LoaiSanPhamBUS bus = new LoaiSanPhamBUS();
        boolean result = bus.xoaLoai(maLoai);

        if (result) {
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            loadDataToTable();  // Cập nhật lại bảng
        } else {
            JOptionPane.showMessageDialog(this, "Xóa thất bại. Kiểm tra ràng buộc dữ liệu.");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

