    private void txtTimKiemKeyReleased(java.awt.event.KeyEvent evt) {
        String keyword = txtTimKiem.getText().trim();
        if (keyword.isEmpty()) {
            loadDataToTable();
            return;
        }
        
        ArrayList<SanPhamDTO> ketQua = banHangBUS.searchSanPham(keyword);
        DefaultTableModel model = (DefaultTableModel) tblSanpham.getModel();
        model.setRowCount(0);
        
        for (SanPhamDTO sp : ketQua) {
            model.addRow(new Object[]{
                sp.MaSP,
                sp.MaLoai,
                sp.TenSP,
                String.format("%,.0f", sp.DonGia),
                sp.SoLuong
            });
        }
    }

