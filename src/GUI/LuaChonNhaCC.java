    private void timKiemTuDong() {
        String tieuChi = cboTimKiem.getSelectedItem().toString();
        String tuKhoa = txtTimKiem.getText().trim();

        NhaCungCapBUS bus = new NhaCungCapBUS();
        ArrayList<NhaCungCapDTO> ketQua = bus.timKiem(tieuChi, tuKhoa);

        DefaultTableModel model = new DefaultTableModel();
        Vector header = new Vector();
        header.add("STT");
        header.add("M√£ NCC");
        header.add("T√™n NCC");
        header.add("ƒê·ªãa ch·ªâ");
        header.add("S·ªë ƒêT");
        model.setColumnIdentifiers(header);

        int i = 1;
        for (NhaCungCapDTO ncc : ketQua) {
            Vector row = new Vector();
            row.add(i++);
            row.add(ncc.getMaNCC());
            row.add(ncc.getTenNCC());
            row.add(ncc.getDiaChi());
            row.add(ncc.getSoDT());
            model.addRow(row);
        }

        tblNhaCC.setModel(model);
    }
